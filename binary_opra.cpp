#include <iostream>
using namespace std;

int main()
{
	int a = 8, b = 3;

	// Addition operator
	cout << "a + b = " << (a + b) << endl; // 11
	
	// Subtraction operator
	cout << "a - b = " << (a - b) << endl;   // 5
	
	// Multiplication operator
	cout << "a * b = " << (a * b) << endl;  // 24
	
	// Division operator
	cout << "a / b = " << (a / b) << endl;  // 2
	
	// Modulo operator
	cout << "a % b = " << (a % b) << endl;  // 2

	return 0;
}
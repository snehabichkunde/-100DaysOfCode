// to determine the size of different data types
#include<iostream>
using namespace std;
int main()
{
    cout << sizeof(char) << endl;
    // size of char is 1 byte
    cout << sizeof(int) << endl;
    // size of int os 4 byte
    cout << sizeof(float) << endl;
    // size of float is 4 byte
    cout << sizeof(bool) << endl;
    // size of boolen is 1 byte
    return 0;
}